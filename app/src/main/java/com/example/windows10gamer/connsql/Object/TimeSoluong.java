public class TimeSoluong {
    int gio, soluong;

    public TimeSoluong(int gio, int soluong) {
        this.gio = gio;
        this.soluong = soluong;
    }

    public int getGio() {
        return gio;
    }

    public void setGio(int gio) {
        this.gio = gio;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
}
